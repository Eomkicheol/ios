private extension UIColor {
  static func color(light: Int, dark: Int) -> UIColor {
    guard #available(iOS 13, *) else { return light.color }
    return UIColor { traitCollection in
      if case .dark = traitCollection.userInterfaceStyle {
        return dark.color
      } else {
        return light.color
      }
    }
  }
}
